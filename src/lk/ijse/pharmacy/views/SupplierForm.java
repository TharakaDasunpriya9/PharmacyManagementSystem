    private void getAll() {
        try {
            ArrayList<Supplier> allsuppliers = SupplierController.getAllSuppliers();
            DefaultTableModel dtm = (DefaultTableModel)tblSupplier.getModel();
            dtm.setRowCount(0);
            for (Supplier s1: allsuppliers) {
                Object[] rowData = {
                    s1.getSupplier_id(),
                    s1.getSupplier_name(),
                    s1.getSupplier_address(),
                    s1.getSupplier_email(),
                    s1.getSupplier_tele(),
                    s1.getSupplier_regNo()};
                dtm.addRow(rowData);
                
            }   } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(SupplierForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
