    private void btnSAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSAddActionPerformed
        try {
            String SID=txtSID.getText();
            String Sname =txtSname.getText();
            String Saddress=txtSaddress.getText();
            String Semail=txtSEmail.getText();
            Integer Stele = Integer.parseInt(txtSTele.getText());
         
            String Sgov=txtSGov.getText();
            
            Supplier s1=new Supplier(
                    SID,
                    Sname,
                    Saddress,
                    Semail,
                    Stele,
                    Sgov
                    
            );
            boolean isSaved=new SupplierController().saveSupplier(s1);
            if(isSaved){
                JOptionPane.showMessageDialog(null, "Saved");
                   DefaultTableModel dtm = (DefaultTableModel)tblSupplier.getModel();
				Object[] rowData = {
					s1.getSupplier_id(),
                                    s1.getSupplier_name(),
                                    s1.getSupplier_address(),
                                    s1.getSupplier_email(),
                                    s1.getSupplier_tele(),
                                    s1.getSupplier_regNo(),
				};
				dtm.addRow(rowData);
				txtSID.setText(null);
				txtSname.setText(null);
				txtSaddress.setText(null);
				txtSEmail.setText(null);
				txtSTele.setText(null);
                                txtSGov.setText(null);
                                
            }else{
                JOptionPane.showMessageDialog(null, "Try again");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this,ex.getMessage());
        }
            
        
        
        
        
    }//GEN-LAST:event_btnSAddActionPerformed

