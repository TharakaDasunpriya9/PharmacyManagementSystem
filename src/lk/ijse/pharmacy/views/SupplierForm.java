    private void txtSIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSIDActionPerformed
         if(txtSID.getText().trim().length()>0){
             try {
                 Supplier OBJ=new SupplierController().getSupplierAdd(txtSID.getText().trim());
                 
                 
                 if(OBJ!=null){
                     txtSID.setText(OBJ.getSupplier_id());
                     txtSname.setText(OBJ.getSupplier_name());
                     txtSaddress.setText(OBJ.getSupplier_address());
                     txtSEmail.setText(OBJ.getSupplier_email());
                     txtSTele.setText(OBJ.getSupplier_tele().toString());
                     txtSGov.setText(OBJ.getSupplier_regNo());
                     
                     
                 }else{
                     JOptionPane.showMessageDialog(this, "No results Found","Warning",JOptionPane.WARNING_MESSAGE);
                 }
             } catch (ClassNotFoundException | SQLException ex) {
                 Logger.getLogger(SupplierForm.class.getName()).log(Level.SEVERE, null, ex);
             }
         }else{
            JOptionPane.showMessageDialog(this, "Please Insert an ID","Error",JOptionPane.ERROR_MESSAGE);
        }
        
        
        
        
        
        
    }//GEN-LAST:event_txtSIDActionPerformed

